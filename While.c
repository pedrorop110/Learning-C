#include <stdio.h>

int main ()
{
    int contador = 1;
    while (contador <= 10) // Enquanto ( contador for menor ou igual a 10)
    {
        printf("%d\n", contador); //printa " contador "
        ++contador; //Adiciona +1
    };
    
/*
    Enquanto ( contador for menor ou igual a 10) ele ira printar o numero 
*/

    return 0;
}