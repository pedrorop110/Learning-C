#include <stdio.h>

int main ()
{
    int inteiro = 12;   // Int para numeros inteiros
    float quebrado = 10.2;  // Float para numeros quebrados
    char letra = 'a';   // Char para letras

    printf("%c\n",letra);
    printf("%d\n",inteiro);
    printf("%f\n",quebrado);

    return 0;
}